#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*list;

	list = ft_lstlast(*lst);
	if (list == NULL)
	{
		*lst = new;
	}
	else if (new != NULL)
	{
		list->next = new;
	}
}
